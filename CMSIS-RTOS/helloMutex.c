/* USER CODE BEGIN 4 */
void Task1(void const * argument){
	uint32_t temp;
	uint32_t counter = 0;
	for(;;)
	{
		osMutexWait(mutexHandle, osWaitForever);
		for(int i = 0; i < 10000; i++)
			count++;
		osMutexRelease(mutexHandle);
		temp = osKernelSysTick() - (1000 * counter++);
		osDelay(1000 - temp);
	}
}
